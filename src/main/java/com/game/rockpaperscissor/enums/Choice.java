package com.game.rockpaperscissor.enums;

/**
 * Enum representing the possible choices in the Rock-Paper-Scissors game.
 */
public enum Choice {
    /**
     * Represents the choice of Rock.
     */
    ROCK,

    /**
     * Represents the choice of Paper.
     */
    PAPER,

    /**
     * Represents the choice of Scissors.
     */
    SCISSORS;
}
